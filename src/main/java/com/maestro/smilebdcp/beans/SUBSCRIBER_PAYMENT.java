package com.maestro.smilebdcp.beans;

import java.sql.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;



@Entity

@Table(name = "SUBSCRIBER_PAYMENT")

public class SUBSCRIBER_PAYMENT {
 
	  @Id
	  private Long ID;
	 
		@Column
	  private String PHONE_NUMBER;
	 
		@Column
	  private Long STATION_ID;
	
		@Column
	  private Double PAY_AMT;
	  
		@Column
	  private String PAY_TYPE;
	
		@Column
	  private String REMARKS;
	
		@Column
	  private Date PAY_DATE;
	  
		@Column
	  private Byte POSITIVE_NEGATIVE_PAYMENT;

		@Column
	  private Long CC_PAYMENT_ID;

	

	 
	 
	 
	 
		public Long getID() {
			return ID;
		}

		public void setID(Long iD) {
			ID = iD;
		}

		public String getPHONE_NUMBER() {
			return PHONE_NUMBER;
		}

		public void setPHONE_NUMBER(String pHONE_NUMBER) {
			PHONE_NUMBER = pHONE_NUMBER;
		}

		public Long getSTATION_ID() {
			return STATION_ID;
		}

		public void setSTATION_ID(Long sTATION_ID) {
			STATION_ID = sTATION_ID;
		}

		public Double getPAY_AMT() {
			 
			return PAY_AMT;
		}

		public void setPAY_AMT(Double pAY_AMT) {
			PAY_AMT = pAY_AMT;
		}

		public String getPAY_TYPE() {
			return PAY_TYPE;
		}

		public void setPAY_TYPE(String pAY_TYPE) {
			PAY_TYPE = pAY_TYPE;
		}

		public String getREMARKS() {
			return REMARKS;
		}

		public void setREMARKS(String rEMARKS) {
			REMARKS = rEMARKS;
		}

		public Date getPAY_DATE() {
			return PAY_DATE;
		}

		public void setPAY_DATE(Date pAY_DATE) {
			PAY_DATE = pAY_DATE;
		}

		public Byte getPOSITIVE_NEGATIVE_PAYMENT() {
			return POSITIVE_NEGATIVE_PAYMENT;
		}

		public void setPOSITIVE_NEGATIVE_PAYMENT(Byte pOSITIVE_NEGATIVE_PAYMENT) {
			POSITIVE_NEGATIVE_PAYMENT = pOSITIVE_NEGATIVE_PAYMENT;
		}

		public long getCC_PAYMENT_ID() {
			return CC_PAYMENT_ID;
		}

		public void setCC_PAYMENT_ID(Long cC_PAYMENT_ID) {
			CC_PAYMENT_ID = cC_PAYMENT_ID;
		}

	
	
	
	
	
	
	
}

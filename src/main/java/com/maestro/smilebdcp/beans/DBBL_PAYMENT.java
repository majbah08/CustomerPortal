package com.maestro.smilebdcp.beans;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

@Entity
@Table(name = "DBBL_PAYMENT")
public class DBBL_PAYMENT {
	@Column
	private String MERCHANT_ID;
	@Column
	private Double TOTAL_AMOUNT;
	@Column
	private Double MAIN_AMMOUNT;
	@Column
	private Double DBBL_AMMOUNT;
	@Column
	private Double COMM_AMOUNT;
	@Column
	private Date TXN_DATE;
	@Column
	private String TRANS_ID;
	@Column
	private String TRANS_DESC;
	@Column
	private Integer CARD_TYPE;
	@Column
	private String TXN_RESULT;
	@Column
	private String TXN_RESULT_CODE;
	
	
	
	
	
	public String getMERCHANT_ID() {
		return MERCHANT_ID;
	}
	public void setMERCHANT_ID(String mERCHANT_ID) {
		MERCHANT_ID = mERCHANT_ID;
	}
	public Double getTOTAL_AMOUNT() {
		return TOTAL_AMOUNT;
	}
	public void setTOTAL_AMOUNT(Double tOTAL_AMOUNT) {
		TOTAL_AMOUNT = tOTAL_AMOUNT;
	}
	public Double getMAIN_AMMOUNT() {
		return MAIN_AMMOUNT;
	}
	public void setMAIN_AMMOUNT(Double mAIN_AMMOUNT) {
		MAIN_AMMOUNT = mAIN_AMMOUNT;
	}
	public Double getDBBL_AMMOUNT() {
		return DBBL_AMMOUNT;
	}
	public void setDBBL_AMMOUNT(Double dBBL_AMMOUNT) {
		DBBL_AMMOUNT = dBBL_AMMOUNT;
	}
	public Double getCOMM_AMOUNT() {
		return COMM_AMOUNT;
	}
	public void setCOMM_AMOUNT(Double cOMM_AMOUNT) {
		COMM_AMOUNT = cOMM_AMOUNT;
	}
	public Date getTXN_DATE() {
		return TXN_DATE;
	}
	public void setTXN_DATE(Date tXN_DATE) {
		TXN_DATE = tXN_DATE;
	}
	public String getTRANS_ID() {
		return TRANS_ID;
	}
	public void setTRANS_ID(String tRANS_ID) {
		TRANS_ID = tRANS_ID;
	}
	public String getTRANS_DESC() {
		return TRANS_DESC;
	}
	public void setTRANS_DESC(String tRANS_DESC) {
		TRANS_DESC = tRANS_DESC;
	}
	public Integer getCARD_TYPE() {
		return CARD_TYPE;
	}
	public void setCARD_TYPE(Integer cARD_TYPE) {
		CARD_TYPE = cARD_TYPE;
	}
	public String getTXN_RESULT() {
		return TXN_RESULT;
	}
	public void setTXN_RESULT(String tXN_RESULT) {
		TXN_RESULT = tXN_RESULT;
	}
	public String getTXN_RESULT_CODE() {
		return TXN_RESULT_CODE;
	}
	public void setTXN_RESULT_CODE(String tXN_RESULT_CODE) {
		TXN_RESULT_CODE = tXN_RESULT_CODE;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
}
